package JavaCoding;
// Kilometers to Miles Examples
// We know that 1 kilometer is approximately equal to 0.62137119 miles.


public class KmToMiles {
    public static void main(String[] args) {
        int km=5;
        // one 1km=0.6213
        System.out.println("5 Kilometer "+(km*0.6213));
        
    }
    
}
