package JavaCoding2;

public class Swap {
    public static void main(String[] args) {
        int num1=20;
        int num2=30;
        num1=num1+num2;
        num2=num1-num2;
        num1=num1-num2;
        System.out.println("First Number: "+num1);
        System.out.println("Second Number:"+num2);

    }
    
}
